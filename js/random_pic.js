function new_text(random_int){
    var text_div= $(".random_img_click_block")
    if (text_div.text()==""){
        text_div.after("<p>点我继续生成，当前随机数为"+String(random_int)+"</p>")
    }
    else{
        $(".random_img_click_block>p").text("点我继续生成，当前随机数为"+String(random_int))
    }

}
